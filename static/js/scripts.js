var emailUrl = window.location.href.indexOf("emails")
var inappUrl = window.location.href.indexOf("inapps")

function channelSwitcher() {
    console.log('execute channelSwitcher')
    if (emailUrl != -1) {
        renderMsg(selectBoxesEmail, previews)
    } else if (inappUrl != -1) {
        renderMsg(selectBoxesInapp, previews)
    }
    previewPane.innerHTML = completeRender
}

function renderMsg(selectArray, previews) {
    console.log('execute renderMsg')
    completeRender = ''
    selectArray.forEach((selectBox) => {
        var selectId = selectBox.options[selectBox.selectedIndex].id
        console.log('select-id:', selectId)
        previews.forEach((preview) => {
            if (selectId === preview[0]) {
                completeRender = completeRender.concat(preview[1])
                debugLoop(selectId)
            }
        })
    })
    if (completeRender != '') {
        previewText.style.display = "none"
    } else {
        previewText.style.display = "block"
    }
}

function debugLoop (selectId) {
    console.log("you have selected:", selectId)
    console.log("concat for: ", selectId)
}

//RUN SCRIPT
if (emailUrl != -1) {
    selectBoxesEmail.forEach((selectBox) => {
        selectBox.addEventListener("change", channelSwitcher)
    })
} else if (inappUrl != -1) {
    selectBoxesInapp.forEach((selectBox) => {
        selectBox.addEventListener("change", channelSwitcher)
    })
}